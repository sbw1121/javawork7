package cn.edu.guet;

public class Ticket {
    private String trainNumber;
    private String fronStation;
    private String toStation;
    private String departureTime;
    private String arrvialTime;
    private String duration;

    public String getTrainNumber() {
        return trainNumber;
    }

    public void setTrainNumber(String trainNumber) {
        this.trainNumber = trainNumber;
    }

    public String getFronStation() {
        return fronStation;
    }

    public void setFronStation(String fronStation) {
        this.fronStation = fronStation;
    }

    public String getToStation() {
        return toStation;
    }

    public void setToStation(String toStation) {
        this.toStation = toStation;
    }

    public String getDepartureTime() {
        return departureTime;
    }

    public void setDepartureTime(String departureTime) {
        this.departureTime = departureTime;
    }

    public String getArrvialTime() {
        return arrvialTime;
    }

    public void setArrvialTime(String arrvialTime) {
        this.arrvialTime = arrvialTime;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }


}
